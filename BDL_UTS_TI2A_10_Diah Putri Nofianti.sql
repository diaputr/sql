--tvf
CREATE FUNCTION Absensi(@nopeg int)
    RETURNS TABLE
    AS
    RETURN (
        SELECT p.nopeg, p.nama, a.tanggal,a.sakit, a.ijin, a.alpha 
		FROM T_Pegawai_Diah p
        JOIN T_Absensi_Diah a 
		ON p.nopeg = a.nopeg
        WHERE p.nopeg = @nopeg
    );

