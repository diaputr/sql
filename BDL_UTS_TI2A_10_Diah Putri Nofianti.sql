SELECT p.nopeg, p.nama, YEAR(a.tanggal) tahun, 
(SUM(p.gaji_pokok + p.tunjangan) -
((SUM(a.alpha)*100000) + (SUM(a.sakit)*20000) + (SUM(a.ijin)*20000))) pendapatan
FROM T_Pegawai_Diah p
JOIN T_Absensi_Diah a on p.nopeg = a.nopeg
GROUP BY p.nopeg, p.nama, YEAR(a.tanggal);

