 CREATE VIEW RekapAbsensi4Bulan AS
 SELECT p.nopeg, SUM(a.ijin) total_ijin, MONTH(a.tanggal) bulan
 FROM T_Pegawai_Diah p
 JOIN T_Absensi_Diah a
 ON a.nopeg = p.nopeg
 WHERE MONTH(a.tanggal) < 5
 GROUP BY p.nopeg, MONTH(a.tanggal);

 SELECT * FROM RekapAbsensi4Bulan R
 PIVOT ( SUM(R.total_ijin) FOR R.bulan IN ([1],[2],[3],[4])) pvt;

